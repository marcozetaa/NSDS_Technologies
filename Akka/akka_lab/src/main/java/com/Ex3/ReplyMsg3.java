package com.Ex3;

public class ReplyMsg3 {

    private String name;
    private String address;

    public ReplyMsg3(String name, String address) {
        this.name = name;
        this.address = address;
    }

    public String getName() {
        return this.name;
    }

    public String getAddress() {
        return this.address;
    }
}
